    <script>
        $(function() {
            var table = $('.data-table').DataTable({
                processing: true,
                serverSide: true,
                ajax: "{{ route('user.index') }}",
                columns: [{
                        data: 'DT_RowIndex',
                        name: 'DT_RowIndex'
                    },
                    {
                        data: 'nama',
                        name: 'nama'
                    },
                    {
                        data: 'role',
                        name: 'role'
                    },
                    {
                        data: 'email',
                        name: 'email'
                    },
                    {
                        data: 'action',
                        name: 'action',
                        orderable: false,
                        searchable: false
                    },
                ]
            });

            $('#addBtn').click(function() {
                $('#saveBtn').val("create-data");
                $('#apotik_id').val('');
                $('#formData').trigger("reset");
                $('#modelHeading').html("Create New Data User");
                $('#formModel').modal('show');
            });

            $('#saveBtn').click(function(e) {
                e.preventDefault();
                var id = $("#id").val();
                var nama = $("#nama").val();
                var role = $("#role").val();
                var image = $("#image").val();
                var email = $("#email").val();
                var password = $("#password").val();
                $.ajax({
                    url: "{{ route('user.store') }}",
                    data: {
                        "_token": "{{ csrf_token() }}",
                        id: id,
                        nama: nama,
                        rola: role,
                        image: image,
                        email: email,
                        password: password,
                    },
                    type: "POST",
                    success: function(data) {
                        $('.modal-close').click();
                        $('#formData').trigger("reset");
                        $('#formModel').modal('hide');
                        table.draw();
                        Swal.fire(
                            'Success',
                            '',
                            'success'
                        );
                    },
                })
            });

            $('body').on('click', '.editData', function() {
                var id_penyakit = $(this).data('id_penyakit');
                $.ajax({
                    type: "POST",
                    url: "{{ route('penyakit.edit') }}",
                    data: {
                        "_token": "{{ csrf_token() }}",
                        id_penyakit: id_penyakit
                    },
                    dataType: 'json',
                    success: function(data) {
                        $('#modelHeading').html("Edit Penyakit");
                        $('#saveBtn').val("edit-data");
                        $('#formModel').modal('show');
                        $('#id_penyakit').val(data.id_penyakit);
                        $('#nama_penyakit').val(data.nama_penyakit);
                        if (data.defunct_ind == "Y") {
                            document.getElementById("defunct_ind").checked = true;
                        } else {
                            document.getElementById("defunct_ind").checked = false;
                        }
                    }
                });
            });
        });
    </script>
